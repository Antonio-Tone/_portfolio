  methods: {
    initialize() {
      this.setInitialStates();
      this.overlay();
      this.overlay2();
    },
    //  Setting the initial states
    setInitialStates() {
      gsap.set(".image__overlay img", {
        opacity: 0,
        scale: 1.08,
      });
      gsap.set("image__overlay", {}),
        gsap.set(".overlay-2 .image-2", {
          opacity: 0,
          scale: 1.08,
        });

      this.layers = [...document.querySelectorAll(".image__overlay")];
      this.images = [...document.querySelectorAll(".image")];
      this.layers2 = [...document.querySelectorAll(".overlay-2")];
      this.images2 = [...document.querySelectorAll(".image-2")];
    },
    overlay() {
      const tl = gsap.timeline({
        scrollTrigger: {
          trigger: ".download",
          scrub: true,
          end: "top top",
          duration: 3,
        },
      });
      this.layers.forEach((layer) => {
        tl.to(layer, {
          ease: "linear",
          yPercent: -10,
        });
      });
      this.images.forEach((image) => {
        tl.to(image, {
          ease: "linear",
          scale: 1,
          opacity: 0.5,
        });
      });
    },

    //  gsap for second image

    overlay2() {
      const tl = gsap.timeline({
        scrollTrigger: {
          trigger: ".wapper",
          scrub: true,
          start: "top bottom",
          duration: 1,
        },
      });
      this.layers2.forEach((layer) => {
        tl.to(layer, {
          ease: "linear",
        });
      });
      this.images2.forEach((image) => {
        tl.to(image, {
          ease: "linear",
          scale: 1,
          opacity: 0.9,
        });
      });
    },
  },
};
